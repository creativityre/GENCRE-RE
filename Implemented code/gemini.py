import pandas as pd
import numpy as np
from sentence_transformers import SentenceTransformer, util

# --- Load both CSVs ---
df_gpt = pd.read_csv("/content/selected_requirements_per_cluster.csv")
df_gemini = pd.read_csv("/content/gemini_IEEE_Standard_Requirements (1).csv")



# --- Rename columns to standardize ---
df_gpt = df_gpt.rename(columns={"selected_requirement": "gpt_req", "cluster_name": "cluster"})
df_gemini = df_gemini.rename(columns={"ieee_requirement": "gemini_req", "cluster_name": "cluster"})

# --- Load a strong sentence embedding model ---
model = SentenceTransformer('all-mpnet-base-v2')

selected_pairs = []

# --- Compare each GPT requirement to all Gemini requirements in the same cluster ---
for cluster, gpt_row in df_gpt.groupby("cluster"):
    gpt_text = gpt_row["gpt_req"].iloc[0]
    gemini_group = df_gemini[df_gemini["cluster"] == cluster]

    if gemini_group.empty:
        continue  # skip if no Gemini data for this cluster

    # Compute embeddings
    emb_gpt = model.encode(gpt_text, convert_to_tensor=True)
    emb_gemini = model.encode(gemini_group["gemini_req"].tolist(), convert_to_tensor=True)

    # Compute cosine similarity
    similarities = util.cos_sim(emb_gpt, emb_gemini)[0].cpu().numpy()

    # Pick the Gemini requirement with the highest similarity
    best_idx = np.argmax(similarities)
    best_req = gemini_group.iloc[best_idx]["gemini_req"]
    best_score = similarities[best_idx]

    selected_pairs.append({
        "cluster": cluster,
        "gpt_requirement": gpt_text,
        "best_gemini_requirement": best_req,
        "similarity_score": round(float(best_score), 4)
    })

# --- Save results ---
df_selected = pd.DataFrame(selected_pairs)
df_selected.to_csv("gemini_best_matched_to_gpt.csv", index=False)

print("✅ Saved most similar Gemini requirements per cluster to gemini_best_matched_to_gpt.csv")


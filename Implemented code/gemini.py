def convert_to_ieee(text):
    """
    Convert a functional or boilerplate requirement into IEEE 1012-2024 style phrasing.
    """
    if not isinstance(text, str) or not text.strip():
        return text

    prompt = f"""
You are an expert in IEEE 1012-2024 software requirements specification.
Rephrase the following requirement into IEEE-compliant phrasing with correct grammar,
precise technical tone, and modal verbs such as "shall", "must", or "should" as appropriate.
Output ONLY the rephrased version — no commentary or explanations.

Requirement:
{text}
"""

    try:
        response = model.generate_content(prompt)
        # time.sleep(2)  # Pause between requests to avoid rate limits

        # Safely extract Gemini output
        if hasattr(response, "text") and response.text:
            return response.text.strip()
        elif hasattr(response, "candidates") and len(response.candidates) > 0:
            return response.candidates[0].content.parts[0].text.strip()
        else:
            print(" Empty response for:", text[:60])
            return text

    except Exception as e:
        print(f" Error for requirement: {text[:60]}... — {e}")
        time.sleep(5)
        return text

